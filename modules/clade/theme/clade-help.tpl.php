<h5>Thank you for your interest in seeing our community grow!</h5>
<p>
	As a new clade manager this document is meant to introduce some of the features and tools that are available to managers. &nbsp;The most noticeable changes are three additional tabs that are present on the home page of the clade you are managing: Edit, Children, and Broadcast.
</p>

<p class="rtecenter">
	<img alt="" src="/sites/default/files/u3/untitled.png" style="width: 432px; height: 56px; " />
</p>

<h5>Edit</h5>
<p>
	The Edit tab contains tools that allow you to add content to the clade homepage such as a clade welcome message or links to pertinent websites.
</p>

<p>
	Upon opening the Edit tab you will see a field containing the title of your clade as well as a description, it is important to note that any changes that are made to the title field will change the name of your clade everywhere it appears in the site including the main tree on the My-Plant.org homepage.
</p>

<p>
	Next you will see a field for a Clade Welcome Message, you can use this to provide a description of your clade, which will appear on your Clade Homepage under the title.
</p>

<p class="rtecenter">
	<img alt="" src="/sites/default/files/u3/untitled_2.png" style="width: 432px; height: 193px; " />
</p>

<p>
	The next section containing three fields with check boxes. &nbsp;The first allows you to add your clade to a list of clades that appear on the registration form for new users. &nbsp;This is intended to help new users quickly sign up for clades they know they are interested in from the start.
</p>

<p>
	The Membership Requests field controls how new members are added to the clade. &nbsp;The default setting is Open allowing anyone who is interested in the clade to become a member with no approval by an administrator. &nbsp;My-Plant encourages you to keep this selection as Open since we feel it best facilitates the building of a community.
</p>

<p>
	The final field controls whether or not the clade is active, it is important to note that unchecking this box will result in the clade being inactive and thus inaccessible to users.
</p>

<p class="rtecenter">
	<img alt="" src="/sites/default/files/u3/untitled_3.png" style="width: 432px; height: 201px; " />
</p>

<p>
	The last tool under the Edit tab allows for the addition of links to external websites. &nbsp;There are three that My-Plant has chosen to include as line items (Encyclopedia of Life Web URL, Tree of Life ID, NCBI Taxonomy ID). &nbsp;Since there is no automated way to create links to specific parts of these three websites we encourage you to visit them and copy and paste their URL into the corresponding field. &nbsp;Note for the NCBI Taxonomy ID link it is only necessary to provide the final four or five digit numerical value of the URL not the entire URL; for example the full URL for Lamiaceae is <a href="http://www.ncbi.nlm.nih.gov/Taxonomy/Browser/wwwtax.cgi?mode=info&amp;id=4136" target="_blank">http://www.ncbi.nlm.nih.gov/Taxonomy/Browser/wwwtax.cgi?mode=info&amp;id=4136</a> however all that needs to be entered into the field is 4136.
</p>

<p class="rtecenter">
	<img alt="" src="/sites/default/files/u3/untitled_4.png" style="width: 432px; height: 181px; " />
</p>

<p>
	Additional links that you see pertinent for your clade can be added using the Clade Links. &nbsp;These links are displayed on the Clade Homepage and can only be created or removed by the clade manager.
</p>

<p>
	Important Reminder: &nbsp;In order for any changes made from within the Edit tab to become active you must click the save button at the bottom of the screen.
</p>

<h5>Children</h5>
<p>
	The second tab available to mangers is the Children tab. &nbsp;This tab simply shows the clades that are nested under yours.
</p>

<h5>Broadcast</h5>
<p>
	The third tab available to managers is the Broadcast tab. &nbsp;This feature allows managers to send a message to all the members of their particular clade.
</p>

<h5>Creating Forums</h5>
<p>
	Currently only clade managers are able to create new forums. &nbsp;Forums are intended to be broader categories, such as &lsquo;conservation&rsquo;, which could contain many threads with a variety of subjects. &nbsp;There is currently only one default forum for new clades, General Discussion, and when users post new topics this is their only forum choice. &nbsp;Clade managers can create new forums to help organize or compartmentalize discussions by clicking on the &lsquo;Manage clade forums&rsquo; link located in the first box on the right-hand side of their screen (see below). &nbsp;And then choosing &lsquo;add forum&rsquo; located directly under Operations. &nbsp;Similarly after a forum is created it can be removed by selecting &lsquo;edit forum&rsquo; from within the &lsquo;Manage clade forums&rsquo; page.
</p>

<p class="rtecenter">
