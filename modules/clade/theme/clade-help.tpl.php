<h5>Additional</h5>
<p>
	In the unlikely event that inappropriate material is posted the clade manager has the ability to edit/remove any content that is posted within their clade. &nbsp;This is done by opening the material and clicking on the Edit tab at the top of a post/forum topic/photo then either editing or deleting the material in question. &nbsp;The process is identical to editing a post that you created, only as clade manager you have access to all the material within the clade regardless of who posted it.
</p>

